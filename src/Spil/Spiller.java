package Spil;

public class Spiller {

    // Vi initierer et privatnavn, fordi navnet kun skal bruges i Spiller-klassen.
    private String navn;

    // Setter navn
    public void setNavn(String navn) {
        this.navn = navn;
    }

    // Getter navn
    public String getNavn() {
        return this.navn;
    }


}
